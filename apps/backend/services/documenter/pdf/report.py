# PAGE 5 — NARRATIVE + APPENDIX
# ============================================================

def _draw_narrative_and_appendix(
    c: canvas_mod.Canvas,
    analysis: AnalysisResult,
    narrative: NaturalReport,
    material: MaterialProperties,
) -> None:
    width, height = theme.PAGE_SIZE
    y = height - 30 * mm

    _section_title(c, y, "7. Engineering Narrative")
    y -= 14 * mm

    c.setFont(*theme.FONT_H2)
    c.setFillColor(theme.INK)
    c.drawString(theme.MARGIN_PT, y, "Summary")
    y -= 5 * mm
    c.setFont(*theme.FONT_BODY)
    c.setFillColor(theme.INK)
    y = _draw_wrapped(c, narrative.summary, theme.MARGIN_PT, y, width - 2 * theme.MARGIN_PT)

    for heading, items in (
        ("Risks", narrative.risks),
        ("Recommendations", narrative.suggestions),
        ("Engineering analogies", narrative.analogies),
    ):
        if not items:
            continue
        y -= 4 * mm
        c.setFont(*theme.FONT_H2)
        c.setFillColor(theme.INK)
        c.drawString(theme.MARGIN_PT, y, heading)
        y -= 5 * mm
        c.setFont(*theme.FONT_BODY)
        c.setFillColor(theme.INK)
        for item in items:
            y = _draw_wrapped(c, f"·  {item}", theme.MARGIN_PT + 4, y, width - 2 * theme.MARGIN_PT - 4)
            y -= 1 * mm

    y -= 6 * mm
    _section_title(c, y, "8. Appendix — Formula Derivation")
    y -= 12 * mm
    c.setFont(*theme.FONT_MONO_BOLD)
    c.setFillColor(theme.INK)
    c.drawString(theme.MARGIN_PT, y, analysis.formula)
    y -= 6 * mm
    c.setFont(*theme.FONT_BODY)
    c.setFillColor(theme.INK)
    derivation = _DERIVATIONS.get(
        analysis.intent_type,
        "Derivation reference: see docs/superpowers/specs/2026-05-16-s3-physics-design.md.",
    )
    y = _draw_wrapped(c, derivation, theme.MARGIN_PT, y, width - 2 * theme.MARGIN_PT)

    y -= 6 * mm
    c.setFont(*theme.FONT_H2)
    c.setFillColor(theme.INK)
    c.drawString(theme.MARGIN_PT, y, "Facts cited by narrative")
    y -= 5 * mm
    c.setFont(*theme.FONT_MONO)
    c.setFillColor(theme.INK_MUTED)
    y = _draw_wrapped(
        c,
        ", ".join(narrative.facts_used) or "—",
        theme.MARGIN_PT,
        y,
        width - 2 * theme.MARGIN_PT,
    )

    y -= 6 * mm
    c.setFont(*theme.FONT_H2)
    c.setFillColor(theme.INK)
    c.drawString(theme.MARGIN_PT, y, "Approvals")
    y -= 14 * mm
    # Signature lines
    line_w = 60 * mm
    c.setStrokeColor(theme.INK)
    c.setLineWidth(theme.LINE_MEDIUM)
    for i, role in enumerate(("Drawn by", "Checked by", "Approved by")):
        x = theme.MARGIN_PT + i * (line_w + 6 * mm)
        c.line(x, y, x + line_w, y)
        c.setFont(*theme.FONT_SMALL)
        c.setFillColor(theme.INK_MUTED)
        c.drawString(x, y - 4 * mm, role.upper())
        c.setFont(*theme.FONT_BODY)
        c.setFillColor(theme.INK)
        default = (
            "MechDesign AI"
            if role == "Drawn by"
            else "Gemma 4"
            if role == "Checked by"
            else "—"
        )
        c.drawString(x, y + 2, default)


