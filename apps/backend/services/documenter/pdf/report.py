"""Build the 5-page engineering report PDF using reportlab."""
from __future__ import annotations

import io
from datetime import UTC, datetime

from reportlab.lib import colors
from reportlab.lib.units import mm
from reportlab.pdfgen import canvas as canvas_mod

from services.documenter.pdf import theme
from services.documenter.pdf.tables import (
    draw_safety_factor_bar,
    draw_table,
    make_kv_table,
    make_table,
)
from services.documenter.pdf.title_block import derive_doc_number
from services.explainer.domain.models import NaturalReport
from services.geometry.domain.artifacts import CachedArtifacts
from services.interpreter.domain.intent import DesignIntent
from services.interpreter.domain.materials import MaterialProperties
from services.physics.domain.models import AnalysisResult

_VERSION = "MechDesign AI · S5 Documenter v0.2"
_DOC_KIND = "ENGINEERING DESIGN REPORT"

_DERIVATIONS: dict[str, str] = {
    "Flywheel_Rim": (
        "Centrifugal stress on a thin rim spinning at angular velocity omega "
        "is sigma = rho * omega^2 * R^2 where rho is density and R is outer "
        "radius. The result is the maximum tangential stress at the periphery."
    ),
    "Pelton_Runner": (
        "Hydraulic power is rho_w*g*Q*H*eta. Optimal bucket speed is "
        "u = 0.46 * sqrt(2*g*H). Shaft torque follows from T = P/omega and the "
        "shear stress on the shaft is tau = 16T/(pi*d^3)."
    ),
    "Hinge_Panel": (
        "Wind pressure is P = C_p * 0.5 * rho_air * v^2. The panel is modeled "
        "as a cantilever loaded by P; bending stress sigma = 6PL^2/t^2 with L "
        "the cantilever length and t the thickness."
    ),
}


def build_report_pdf(
    *,
    intent: DesignIntent,
    analysis: AnalysisResult,
    narrative: NaturalReport,
    geometry: CachedArtifacts,
    material: MaterialProperties,
    svg_bytes: bytes,
    now_utc_iso: str,
    cache_key: str,
) -> bytes:
    """Return PDF bytes for the 5-page engineering report."""
    buf = io.BytesIO()
    c = canvas_mod.Canvas(buf, pagesize=theme.PAGE_SIZE)
    doc_number = derive_doc_number(cache_key)

    _draw_cover(c, intent, analysis, material, doc_number, now_utc_iso)
    _draw_page_chrome(c, page_no=1, total=5, doc_number=doc_number, title=_DOC_KIND)
    c.showPage()

    _draw_intent_and_bom(c, intent, material)
    _draw_page_chrome(c, page_no=2, total=5, doc_number=doc_number, title="DESIGN INTENT · BOM")
    c.showPage()

    _draw_geometry(c, geometry, svg_bytes)
    _draw_page_chrome(c, page_no=3, total=5, doc_number=doc_number, title="GEOMETRY · MASS PROPERTIES")
    c.showPage()

    _draw_analysis(c, analysis)
    _draw_page_chrome(c, page_no=4, total=5, doc_number=doc_number, title="STRUCTURAL ANALYSIS")
    c.showPage()

    _draw_narrative_and_appendix(c, analysis, narrative, material)
    _draw_page_chrome(c, page_no=5, total=5, doc_number=doc_number, title="NARRATIVE · APPENDIX")
    c.showPage()

    c.save()
    return buf.getvalue()


# ============================================================
# COVER
# ============================================================

def _draw_cover(
    c: canvas_mod.Canvas,
    intent: DesignIntent,
    analysis: AnalysisResult,
    material: MaterialProperties,
    doc_number: str,
    now_utc_iso: str,
) -> None:
    width, height = theme.PAGE_SIZE

    # Eggplant header band
    band_h = 54 * mm
    c.setFillColor(theme.BROTEA_EGGPLANT)
    c.rect(0, height - band_h, width, band_h, stroke=0, fill=1)

    # Glow yellow accent stripe
    c.setFillColor(theme.BROTEA_GLOW)
    c.rect(0, height - band_h - 2, width, 2, stroke=0, fill=1)

    # Eyebrow
    c.setFillColor(theme.BROTEA_GLOW)
    c.setFont(*theme.FONT_LABEL)
    c.drawString(theme.MARGIN_PT, height - 18 * mm, _DOC_KIND)

    # Title
    c.setFillColor(colors.white)
    c.setFont(*theme.FONT_TITLE)
    c.drawString(theme.MARGIN_PT, height - 32 * mm, "Mechanical Design Report")

    # Subtitle = intent type + doc number
    c.setFont(*theme.FONT_H2)
    c.setFillColor(theme.BROTEA_BONE)
    c.drawString(theme.MARGIN_PT, height - 42 * mm, f"{intent.type.replace('_', ' ')}   ·   {doc_number}")

    # Cover summary grid
    summary_top = height - band_h - 12 * mm
    table = make_kv_table(
        [
            ("Document", doc_number),
            ("Subject", intent.type.replace("_", " ")),
            ("Material", material.name.replace("_", " ")),
            ("Date (UTC)", now_utc_iso),
            ("Revision", "R1"),
            ("Status", analysis.verdict.value.upper()),
            ("Generated by", _VERSION),
            ("Standard", "ISO 7200 (title block layout)"),
        ],
        col_widths=[42 * mm, 100 * mm],
    )
    draw_table(c, table, theme.MARGIN_PT, summary_top, max_width=160 * mm)

    # Verdict badge
    verdict = analysis.verdict.value
    badge_color = theme.COLOR_VERDICT.get(verdict, colors.grey)
    badge_w = 70 * mm
    badge_h = 22 * mm
    badge_x = width - theme.MARGIN_PT - badge_w
    badge_y = summary_top - 30 * mm
    c.setFillColor(badge_color)
    c.roundRect(badge_x, badge_y, badge_w, badge_h, 4, stroke=0, fill=1)
    c.setFillColor(colors.white)
    c.setFont("Helvetica-Bold", 18)
    c.drawCentredString(badge_x + badge_w / 2, badge_y + badge_h / 2 - 4, verdict.upper())
    c.setFont(*theme.FONT_SMALL)
    c.drawCentredString(badge_x + badge_w / 2, badge_y + badge_h / 2 - 14, f"Safety factor {analysis.safety_factor:.2f}")

    # Brand strip near bottom
    c.setFillColor(theme.BROTEA_VIOLET)
    c.rect(0, 38 * mm, width, 4, stroke=0, fill=1)
    c.setFillColor(theme.INK)
    c.setFont(*theme.FONT_LABEL)
    c.drawString(theme.MARGIN_PT, 30 * mm, "MECHDESIGN AI · GEMMA 4 GOOD")
    c.setFillColor(theme.INK_MUTED)
    c.setFont(*theme.FONT_SMALL)
    c.drawString(
        theme.MARGIN_PT,
        24 * mm,
        "Specs and figures in this report are auto-generated from a natural-language intent.",
    )


