def _section_title(c: canvas_mod.Canvas, y: float, label: str) -> None:
    c.setFillColor(theme.BROTEA_EGGPLANT)
    c.rect(theme.MARGIN_PT - 4, y - 1, 4, 8 * mm, stroke=0, fill=1)
    c.setFillColor(theme.INK)
    c.setFont(*theme.FONT_H1)
    c.drawString(theme.MARGIN_PT + 4, y + 1, label)


def _draw_page_chrome(
    c: canvas_mod.Canvas,
    *,
    page_no: int,
    total: int,
    doc_number: str,
    title: str,
) -> None:
    """Draw running header + footer on the current page."""
    width, height = theme.PAGE_SIZE
    # Top rule + crumbs (skip on cover)
    if page_no > 1:
        c.setStrokeColor(theme.RULE)
        c.setLineWidth(theme.LINE_THIN)
        c.line(theme.MARGIN_PT, height - 18 * mm, width - theme.MARGIN_PT, height - 18 * mm)
        c.setFillColor(theme.INK_MUTED)
        c.setFont(*theme.FONT_LABEL)
        c.drawString(theme.MARGIN_PT, height - 14 * mm, f"MECHDESIGN AI  ·  {doc_number}")
        c.drawRightString(width - theme.MARGIN_PT, height - 14 * mm, title)

    # Bottom rule + meta
    c.setStrokeColor(theme.RULE)
    c.setLineWidth(theme.LINE_THIN)
    c.line(theme.MARGIN_PT, 16 * mm, width - theme.MARGIN_PT, 16 * mm)
    c.setFillColor(theme.INK_MUTED)
    c.setFont(*theme.FONT_LABEL)
    c.drawString(theme.MARGIN_PT, 12 * mm, _VERSION)
    c.drawRightString(width - theme.MARGIN_PT, 12 * mm, f"Page {page_no} / {total}")
    c.drawCentredString(width / 2, 12 * mm, f"Generated {datetime.now(tz=UTC).strftime('%Y-%m-%d %H:%MZ')}")


def _try_embed_svg(
    c: canvas_mod.Canvas,
    svg_bytes: bytes,
    x: float,
    y: float,
    w: float,
    h: float,
) -> bool:
    try:
        from reportlab.graphics import renderPDF
        from svglib.svglib import svg2rlg  # type: ignore[import-not-found]
    except ImportError:
        return False
    try:
        drawing = svg2rlg(io.BytesIO(svg_bytes))
        if drawing is None:
            return False
        scale_x = w / drawing.width if drawing.width else 1.0
        scale_y = h / drawing.height if drawing.height else 1.0
        scale = min(scale_x, scale_y)
        drawing.scale(scale, scale)
        renderPDF.draw(drawing, c, x, y)
        return True
    except Exception:
        return False


def _draw_wrapped(
    c: canvas_mod.Canvas,
    text: str,
    x: float,
    y: float,
    max_width: float,
    line_height: float = 11.5,
) -> float:
    """Naive word-wrap with the current font."""
    words = text.split()
    if not words:
        return y
    line = ""
    font_name, font_size = c._fontname, c._fontsize  # type: ignore[attr-defined]
    for w in words:
        candidate = (line + " " + w).strip()
        if c.stringWidth(candidate, font_name, font_size) <= max_width:
            line = candidate
        else:
            c.drawString(x, y, line)
            y -= line_height
            line = w
    if line:
        c.drawString(x, y, line)
        y -= line_height
    return y
