# PAGE 4 — STRUCTURAL ANALYSIS
# ============================================================

def _draw_analysis(
    c: canvas_mod.Canvas,
    analysis: AnalysisResult,
) -> None:
    width, height = theme.PAGE_SIZE
    y = height - 30 * mm

    _section_title(c, y, "6. Structural Analysis")
    y -= 14 * mm

    # Methodology
    c.setFont(*theme.FONT_H2)
    c.setFillColor(theme.INK)
    c.drawString(theme.MARGIN_PT, y, "Methodology")
    y -= 5 * mm
    c.setFont(*theme.FONT_BODY)
    c.setFillColor(theme.INK_MUTED)
    c.drawString(theme.MARGIN_PT, y, f"Intent: {analysis.intent_type}    Material: {analysis.material_name}")
    y -= 5 * mm
    c.setFont(*theme.FONT_MONO_BOLD)
    c.setFillColor(theme.INK)
    c.drawString(theme.MARGIN_PT, y, f"σ_formula:  {analysis.formula}")
    y -= 8 * mm

    # Results table
    stress_mpa = analysis.stress_max_pa / 1e6
    margin_pct = ((analysis.material_yield_mpa - stress_mpa) / analysis.material_yield_mpa) * 100 if analysis.material_yield_mpa else 0.0
    rows: list[list[str]] = [
        ["Quantity", "Value", "Unit", "Allowable", "Margin"],
        [
            "Max stress (σ_max)",
            f"{stress_mpa:.3f}",
            "MPa",
            f"{analysis.material_yield_mpa:.1f}",
            f"{margin_pct:+.1f}%",
        ],
        [
            "Yield strength",
            f"{analysis.material_yield_mpa:.1f}",
            "MPa",
            "—",
            "—",
        ],
        [
            "Max displacement",
            f"{analysis.displacement_max_m * 1000:.4f}",
            "mm",
            "—",
            "—",
        ],
        [
            "Safety factor (SF)",
            f"{analysis.safety_factor:.2f}",
            "—",
            "≥ 2.0",
            "PASS" if analysis.safety_factor >= 2.0 else ("WARN" if analysis.safety_factor >= 1.5 else "FAIL"),
        ],
    ]
    table = make_table(
        rows,
        col_widths=[55 * mm, 28 * mm, 18 * mm, 32 * mm, 27 * mm],
        align_right_cols=(1, 3),
    )
    y = draw_table(c, table, theme.MARGIN_PT, y, max_width=160 * mm)

    y -= 12 * mm
    c.setFont(*theme.FONT_H2)
    c.setFillColor(theme.INK)
    c.drawString(theme.MARGIN_PT, y, "Safety Factor Gauge")
    y -= 14 * mm
    draw_safety_factor_bar(
        c,
        theme.MARGIN_PT,
        y,
        width=140 * mm,
        height=8 * mm,
        safety_factor=analysis.safety_factor,
    )
    y -= 18 * mm
    c.setFont(*theme.FONT_SMALL)
    c.setFillColor(theme.INK_MUTED)
    c.drawString(
        theme.MARGIN_PT,
        y,
        "Thresholds: fail < 1.5 (red)   ·   warn 1.5–2.0 (amber)   ·   pass ≥ 2.0 (green)",
    )
    y -= 10 * mm

    # Verdict block
    verdict = analysis.verdict.value
    badge_color = theme.COLOR_VERDICT.get(verdict, colors.grey)
    bw = 80 * mm
    bh = 20 * mm
    bx = theme.MARGIN_PT
    by = y - bh
    c.setFillColor(badge_color)
    c.roundRect(bx, by, bw, bh, 4, stroke=0, fill=1)
    c.setFillColor(colors.white)
    c.setFont("Helvetica-Bold", 16)
    c.drawString(bx + 6, by + bh / 2 - 3, f"VERDICT: {verdict.upper()}")
    c.setFont(*theme.FONT_SMALL)
    c.drawString(bx + 6, by + 4, f"SF = {analysis.safety_factor:.2f}")

    # Loading conditions
    y = by - 14 * mm
    c.setFont(*theme.FONT_H2)
    c.setFillColor(theme.INK)
    c.drawString(theme.MARGIN_PT, y, "Loading Conditions")
    y -= 4 * mm
    c.setFont(*theme.FONT_BODY)
    c.setFillColor(theme.INK_MUTED)
    notes = analysis.notes or "Operating condition derived from the design intent (see §1)."
    y = _draw_wrapped(c, notes, theme.MARGIN_PT, y - 4 * mm, max_width=width - 2 * theme.MARGIN_PT)


