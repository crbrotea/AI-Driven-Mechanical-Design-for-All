# PAGE 3 — GEOMETRY + MASS PROPERTIES
# ============================================================

def _draw_geometry(
    c: canvas_mod.Canvas,
    geometry: CachedArtifacts,
    svg_bytes: bytes,
) -> None:
    _, height = theme.PAGE_SIZE
    y = height - 30 * mm

    _section_title(c, y, "4. Mass Properties")
    y -= 14 * mm

    m = geometry.mass_properties
    bbox_w = m.bbox_m[3] - m.bbox_m[0]
    bbox_h = m.bbox_m[4] - m.bbox_m[1]
    bbox_d = m.bbox_m[5] - m.bbox_m[2]

    rows: list[list[str]] = [
        ["Quantity", "Value", "Unit"],
        ["Mass", f"{m.mass_kg:.3f}", "kg"],
        ["Volume", f"{m.volume_m3:.5f}", "m³"],
        [
            "Center of mass",
            f"({m.center_of_mass[0]:.3f}, {m.center_of_mass[1]:.3f}, {m.center_of_mass[2]:.3f})",
            "m",
        ],
        ["Bounding box X", f"{bbox_w:.3f}", "m"],
        ["Bounding box Y", f"{bbox_h:.3f}", "m"],
        ["Bounding box Z", f"{bbox_d:.3f}", "m"],
        [
            "Bbox min (x,y,z)",
            f"({m.bbox_m[0]:.3f}, {m.bbox_m[1]:.3f}, {m.bbox_m[2]:.3f})",
            "m",
        ],
        [
            "Bbox max (x,y,z)",
            f"({m.bbox_m[3]:.3f}, {m.bbox_m[4]:.3f}, {m.bbox_m[5]:.3f})",
            "m",
        ],
    ]
    table = make_table(
        rows,
        col_widths=[60 * mm, 70 * mm, 20 * mm],
        align_right_cols=(1,),
    )
    y = draw_table(c, table, theme.MARGIN_PT, y, max_width=160 * mm)

    y -= 12 * mm
    _section_title(c, y, "5. Section View")
    y -= 6 * mm
    c.setFillColor(theme.INK_MUTED)
    c.setFont(*theme.FONT_SMALL)
    c.drawString(theme.MARGIN_PT, y, "Mid-plane section, viewing along +Z. Dimensions in metres.")
    y -= 4 * mm

    # Drawing frame
    frame_w = 150 * mm
    frame_h = 110 * mm
    c.setStrokeColor(theme.INK)
    c.setLineWidth(theme.LINE_MEDIUM)
    c.rect(theme.MARGIN_PT, y - frame_h, frame_w, frame_h, stroke=1, fill=0)
    embedded = _try_embed_svg(
        c,
        svg_bytes,
        theme.MARGIN_PT + 6,
        y - frame_h + 6,
        frame_w - 12,
        frame_h - 12,
    )
    if not embedded:
        c.setFont(*theme.FONT_BODY)
        c.setFillColor(theme.INK_MUTED)
        c.drawCentredString(
            theme.MARGIN_PT + frame_w / 2,
            y - frame_h / 2,
            f"Section view at {geometry.svg_url}",
        )


