# PAGE 2 — DESIGN INTENT + BOM
# ============================================================

def _draw_intent_and_bom(
    c: canvas_mod.Canvas,
    intent: DesignIntent,
    material: MaterialProperties,
) -> None:
    width, height = theme.PAGE_SIZE
    y = height - 30 * mm  # leave room for the running header

    _section_title(c, y, "1. Design Intent")
    y -= 14 * mm

    intent_rows: list[list[str]] = [["Parameter", "Value", "Source", "Original"]]
    for name, field in sorted(intent.fields.items()):
        if field.value is None:
            continue
        value = field.value
        intent_rows.append(
            [
                name,
                f"{value:.4f}" if isinstance(value, (int, float)) else str(value),
                field.source.value,
                field.original or "—",
            ]
        )
    if len(intent_rows) > 1:
        table = make_table(
            intent_rows,
            col_widths=[55 * mm, 35 * mm, 30 * mm, 38 * mm],
            align_right_cols=(1,),
        )
        y = draw_table(c, table, theme.MARGIN_PT, y, max_width=160 * mm)

    y -= 10 * mm
    _section_title(c, y, "2. Bill of Materials")
    y -= 14 * mm

    bom_rows: list[list[str]] = [["Item", "Component", "Qty", "Material", "Role"]]
    bom_rows.append(["1", intent.type.replace("_", " "), "1", material.name.replace("_", " "), "Main"])
    for i, composed in enumerate(intent.composed_of, start=2):
        bom_rows.append(
            [str(i), composed.replace("_", " "), "1", material.name.replace("_", " "), "Companion"]
        )
    table = make_table(
        bom_rows,
        col_widths=[14 * mm, 60 * mm, 14 * mm, 50 * mm, 22 * mm],
        align_right_cols=(2,),
    )
    y = draw_table(c, table, theme.MARGIN_PT, y, max_width=160 * mm)

    y -= 10 * mm
    _section_title(c, y, "3. Material Specification")
    y -= 14 * mm
    mat_rows: list[list[str]] = [["Property", "Value", "Unit"]]
    mat_rows.extend(
        [
            ["Density", f"{material.density_kg_m3:,.0f}", "kg/m³"],
            ["Young modulus", f"{material.young_modulus_gpa:.1f}", "GPa"],
            ["Yield strength", f"{material.yield_strength_mpa:.1f}", "MPa"],
            ["Ultimate tensile", f"{material.ultimate_tensile_strength_mpa:.1f}", "MPa"],
            ["Thermal conductivity", f"{material.thermal_conductivity_w_m_k:.2f}", "W/m·K"],
            ["Max service temp.", f"{material.max_service_temperature_c:.0f}", "°C"],
            ["Relative cost index", f"{material.relative_cost_index:.2f}", "—"],
            ["Sustainability score", f"{material.sustainability_score:.2f}", "—"],
            ["Category", material.category, "—"],
        ]
    )
    table = make_table(
        mat_rows,
        col_widths=[60 * mm, 60 * mm, 30 * mm],
        align_right_cols=(1,),
    )
    draw_table(c, table, theme.MARGIN_PT, y, max_width=160 * mm)


